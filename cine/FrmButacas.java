    private void eventos(JPanel panel) {
        Color currentColor = panel.getBackground();
        Color newColor = currentColor.equals(Color.white) ? Color.decode("#003399") : Color.white; 
        panel.setBackground(newColor);
        if(butacasSeleccionadas.contains(panel.getName())){
            butacasSeleccionadas.remove(panel.getName());
            nButacas--;
        }else{
            butacasSeleccionadas.add(panel.getName());
            nButacas++;
        }
        StringBuilder sb = new StringBuilder("");
        for (String butaca : butacasSeleccionadas) {
            sb.append(butaca).append(", ");
        }
        jLabel75.setText(sb.toString());
        if(nButacas>=1){
            panelRound121.setBackground(new Color(0,51,153));
        }else{
            panelRound121.setBackground(new Color(153,153,153));
        }
        
    }
    private int[][] convertirAMatriz(Set<String> butacasSeleccionadas) {
        int[][] matrizButacas = new int[9][11];
        for (String butaca : butacasSeleccionadas) {
            char filaChar = butaca.charAt(0);
            int columna = Integer.parseInt(butaca.substring(1));
            int fila = filaChar - 'A';
            int col = columna - 1;
            if (fila >= 0 && fila < 9 && col >= 0 && col < 11) {
                matrizButacas[fila][col] = 1;
            }
        }
        return matrizButacas;
    }

    
    
