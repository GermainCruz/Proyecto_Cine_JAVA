package gestionlogin;

/**
 *
 * @author Harry
 */
public class Node {
    String correo;
    int idCliente; // ID del cliente en la BD
    Node left, right;

    public Node(String correo, int idCliente) {
        this.correo = correo;
        this.idCliente = idCliente;
        this.left = this.right = null;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public int getIdCliente() {
        return idCliente;
    }

    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }

    public Node getLeft() {
        return left;
    }

    public void setLeft(Node left) {
        this.left = left;
    }

    public Node getRight() {
        return right;
    }

    public void setRight(Node right) {
        this.right = right;
    }
    
    
}
