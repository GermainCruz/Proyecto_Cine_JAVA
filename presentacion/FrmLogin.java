    private void panelRound5MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelRound5MousePressed
        // TODO add your handling code here:
        String nroSocio = txtNroSocio.getText();
        String contraseña = txtContraseña.getText();

        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bd_peliculas", "root", "Hernan77..")) {
            String query = "SELECT idcliente, clinombre, cliapellido, clicontraseña FROM cliente WHERE numero_documento = ?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1, nroSocio);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                String hashedPassword = rs.getString("clicontraseña");
                if (hashedPassword.equals(contraseña)) { // Comparar hashes
                    int idCliente = Integer.parseInt(rs.getString("idCliente"));
                    String nombre = rs.getString("clinombre");
                    String apellido = rs.getString("cliapellido");

                    // Mostrar mensaje de bienvenida
                    JOptionPane.showMessageDialog(this, "¡Bienvenido, " + nombre + " " + apellido + "!");

                    // Continúa al sistema con el nombre y apellido para la bienvenida
                    FrmPRINCIPAL frame = new FrmPRINCIPAL(idCliente, nombre, apellido);
                    frame.setVisible(true);
                    this.dispose(); // Cierra el login actual
                } else {
                    JOptionPane.showMessageDialog(this, "Contraseña incorrecta");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Usuario no encontrado");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_panelRound5MousePressed

