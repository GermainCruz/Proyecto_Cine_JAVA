    private void cargarDatosArbol() {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/bd_peliculas", "root", "Hernan77..")) {
            String query = "SELECT idcliente, clicorreo FROM cliente";
            PreparedStatement ps = con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                int id = rs.getInt("idcliente");
                String correo = rs.getString("clicorreo");
                tree.insert(correo, id);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

