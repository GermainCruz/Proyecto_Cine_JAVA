    private void comboBoxActionPerformed(java.awt.event.ActionEvent evt) {
        // Obtener el género seleccionado
        String seleccionado = (String) cbxGenero.getSelectedItem();

        // Mostrar el panel correspondiente según la selección
        if (seleccionado.equals("Accion")) {
            mostrarPanel(new PanelAccion());  // Mostrar PanelAccion
        } else if (seleccionado.equals("Animacion")) {
            mostrarPanel(new PanelAnimacion());  // Mostrar PanelAnimacion
        } else if (seleccionado.equals("Drama")) {
            mostrarPanel(new PanelDrama());  // Mostrar PanelDrama
        }
    }

