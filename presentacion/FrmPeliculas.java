    public void verificar() {
        boolean cartelera = false;
        boolean prox_estr = false;
        if (rdbtnCartelera.isSelected()) {
            cartelera = true;
        } else {
            prox_estr = true;
        }

        if (cartelera) {
            mostrarPanel(new PanelCartelera(idCliente));
        } else {
            mostrarPanel(new PanelProximosEstrenos(idCliente));
        }
    }

      private void SetImageLabel(JLabel labelName, String root) {
        ImageIcon image = new ImageIcon(root);
        Icon icon = new ImageIcon(
                image.getImage().getScaledInstance(labelName.getWidth(), labelName.getHeight(), Image.SCALE_DEFAULT)
        );
        labelName.setIcon(icon);
        this.repaint();
    }

    public void mostrarPanel(JPanel panel) {
        panel.setSize(674, 611);  // Ajustar el tamaño del nuevo panel
        panel.setLocation(0, 0);   // Posición sobre el jPanel2
        panel.setBackground(Color.WHITE);  // Fondo blanco

        jPanel2.removeAll();  // Limpiar el contenido actual
        jPanel2.add(panel, BorderLayout.CENTER);  // Añadir el nuevo panel
        jPanel2.revalidate();  // Validar el layout
        jPanel2.repaint();     // Redibujar el panel
    }
