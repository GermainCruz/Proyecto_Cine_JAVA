    private void panelRound1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelRound1MousePressed
        // TODO add your handling code here:
        try {
            // Crear la conexión a la base de datos
            Connection connection = null;
            try {
                connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/bd_peliculas", "root", "Hernan77..");

                // Crear una instancia de la pila de clientes y cargarla con los datos existentes
                PilaCliente pilaClientes = new PilaCliente();
                cargarClientesEnPila(pilaClientes, connection); // Método actualizado que utiliza la conexión
                Cliente cliente = pilaClientes.buscarPorId(idCliente);
//                System.out.println("Cliente encontrado: " + (cliente != null ? cliente.getNombre() : "null"));

                // Verificar si el cliente es registrado o no
                if (idCliente == -1 || idCliente == 0) {
                    JOptionPane.showMessageDialog(null, "Cliente no registrado. Complete los datos manualmente.", "Aviso", JOptionPane.INFORMATION_MESSAGE);

                    // Dejar el campo de celular libre para que lo llenen manualmente
                    txtNroCelular.setText("");
                } else {
                    // Rellenar automáticamente los datos del cliente
                    if (cliente != null) {
                        String tipoDocumento = cliente.getTipoDocumento();
                        cbxDocumento.setSelectedItem(tipoDocumento);
                        txtNroDocumento.setText(cliente.getNumeroDocumento());
                        
                        cbxDocumento.setEnabled(true);
                        txtNroDocumento.setEnabled(true);
                    } else {
                        JOptionPane.showMessageDialog(null, "El cliente no existe en la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }

                // Actualizar la venta con los datos del cliente
                actualizarVentaConCliente(connection, idVenta, idCliente, (String) cbxDocumento.getSelectedItem(), txtNroDocumento.getText());

                // Registrar el método de pago en la base de datos
                String numeroCelular = txtNroCelular.getText();
                actualizarVentaConPago(connection, idVenta, "billetera", null, null, null, numeroCelular);

                // Mensaje de confirmación
                JOptionPane.showMessageDialog(null, "Pago registrado con billetera.", "Éxito", JOptionPane.INFORMATION_MESSAGE);

                // Cerrar la pestaña actual y regresar a FrmPrincipal
                JFrame frameContenedor = (JFrame) SwingUtilities.getWindowAncestor(this); // Obtiene el JFrame padre
                frameContenedor.dispose();
                FrmPRINCIPAL f = new FrmPRINCIPAL(idCliente, cliente.getNombre(), cliente.getApellido());
                f.setVisible(true);

            } catch (SQLException e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error en la conexión a la base de datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            } finally {
                if (connection != null) {
                    try {
                        connection.close(); // Cerrar la conexión
                    } catch (SQLException e) {
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(null, "Error al cerrar la conexión: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                    }
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al registrar el pago con billetera.", "Error", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_panelRound1MousePressed

