    private static void actualizarVentaConCliente(Connection connection, int idVenta, int idCliente, String tipoDocumento, String numeroDocumento) throws SQLException {
        String query = "UPDATE venta SET idcliente = ?, tipo_documento = ?, numero_documento = ? WHERE idventa = ?";
        try (PreparedStatement statement = connection.prepareStatement(query)) {
            statement.setObject(1, idCliente > 0 ? idCliente : null);
            statement.setString(2, tipoDocumento);
            statement.setString(3, numeroDocumento);
            statement.setInt(4, idVenta);
            statement.executeUpdate();
        }
    }

    private static void actualizarVentaConPago(Connection connection, int idVenta, String metodoPago, String numeroTarjeta, String cvv, String fechaVencimiento, String numeroCelular) throws SQLException {
        String query = "UPDATE venta SET metodopago = ?, numero_tarjeta = ?, cvv = ?, fecha_vencimiento = ?, numero_celular = ? WHERE idventa = ?";
        try (PreparedStatement statement = connection.prepareStatement(query)) {
            statement.setString(1, metodoPago);
            statement.setString(2, numeroTarjeta);
            statement.setString(3, cvv);
            statement.setString(4, fechaVencimiento);
            statement.setString(5, numeroCelular);
            statement.setInt(6, idVenta);
            statement.executeUpdate();
        }
    }

    private static String obtenerTipoDocumentoCliente(Connection connection, String numeroDocumento) throws SQLException {
        String query = "SELECT tipo_documento FROM cliente WHERE numero_documento = ?";
        try (PreparedStatement statement = connection.prepareStatement(query)) {
            statement.setString(1, numeroDocumento);
            ResultSet resultSet = statement.executeQuery();

            if (resultSet.next()) {
                return resultSet.getString("tipo_documento");
            }
        }
        return "N/T";
    }

